import { cpSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import * as esbuild from "esbuild";
import { spawnSync } from "node:child_process";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

const shared = spawnSync(process.execPath, [join(root, "scripts/bundle-shared.mjs")], {
  stdio: "inherit",
});
if (shared.status !== 0) process.exit(shared.status ?? 1);

await esbuild.build({
  entryPoints: [join(root, "api/handler.source.ts")],
  outfile: join(root, "api/index.js"),
  bundle: true,
  platform: "node",
  format: "esm",
  target: "node20",
  logLevel: "info",
  packages: "bundle",
  banner: {
    js: "// Generated by scripts/bundle-vercel-api.mjs — do not edit",
  },
});

const appsApiDir = join(root, "apps/api/api");
mkdirSync(appsApiDir, { recursive: true });
cpSync(join(root, "api/index.js"), join(appsApiDir, "index.js"));

console.log("Bundled api/index.js and apps/api/api/index.js");
